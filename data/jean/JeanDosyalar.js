// file ve line parametresi ile içerik döndür
export const getJeanContent = (file, line) => {
  const fileIndex = parseInt(file, 10);
  const lineIndex = parseInt(line, 10) - 1; // 1-indexed

  const fileData = jeanFiles[fileIndex];
  if (fileData && fileData.lines[lineIndex]) {
    return {
      file: fileIndex,
      fileName: fileData.name,
      line: lineIndex + 1,
      text: fileData.lines[lineIndex],
    };
  }

  return {
    file: fileIndex,
    fileName: fileData?.name || "Bilinmeyen Dosya",
    line: lineIndex + 1,
    text: "Geçersiz seans numarası",
  };
};


